package umn.ac.keadaanudara.Model;

public class LocationModel {
    private double lat;
    private double lon;
    private int dayFirst;
    private int daySecond;
    private int dayThird;
    private int dayFourth;
    private int dayFifth;
    private int tomorrow;

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }

    public int getDayFirst() {
        return dayFirst;
    }

    public void setDayFirst(int dayFirst) {
        this.dayFirst = dayFirst;
    }

    public int getDaySecond() {
        return daySecond;
    }

    public void setDaySecond(int daySecond) {
        this.daySecond = daySecond;
    }

    public int getDayThird() {
        return dayThird;
    }

    public void setDayThird(int dayThird) {
        this.dayThird = dayThird;
    }

    public int getDayFourth() {
        return dayFourth;
    }

    public void setDayFourth(int dayFourth) {
        this.dayFourth = dayFourth;
    }

    public int getDayFifth() {
        return dayFifth;
    }

    public void setDayFifth(int dayFifth) {
        this.dayFifth = dayFifth;
    }

    public int getTomorrow() {
        return tomorrow;
    }

    public void setTomorrow(int tomorrow) {
        this.tomorrow = tomorrow;
    }
}
